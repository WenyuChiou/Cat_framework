# Column index mapping (0-based, excluding the period column)
_CI = {name: i for i, name in enumerate(_COL_NAMES)}

# Fast period-to-row-index lookup (rounded to 6 decimals)
_PERIOD_TO_IDX: dict[float, int] = {
    round(p, 6): i for i, p in enumerate(_PERIODS)
}


def _get_row(period: float) -> np.ndarray:
    """Look up coefficient row by exact period match.

    For PGA, use period=0.0.  Raises ValueError if no match.
    """
    key = round(period, 6)
    if key in _PERIOD_TO_IDX:
        return _COEFF_ARRAY[_PERIOD_TO_IDX[key]]
    available_sa = sorted(p for p in _PERIODS if p >= 0)
    raise ValueError(
        f"Period {period}s not in BSSA14/21 table. "
        f"Available SA periods (first 10): {available_sa[:10]}..."
    )


# ── BSSA21 GMPE implementation ──────────────────────────────────────────

class BSSA21:
    """Boore, Stewart, Seyhan & Atkinson (2014/2021) NGA-West2 GMPE."""

    @property
    def name(self) -> str:
        return "bssa21"

    @property
    def supported_periods(self) -> list[float]:
        return [p for p in _PERIODS if p >= 0]  # exclude PGV (-1)

    def compute(
        self,
        Mw: float,
        R_JB: float,
        Vs30: float,
        fault_type: str,
        period: float,
    ) -> tuple[float, float]:
        """Compute median SA (g) and total sigma (ln units).

        Parameters
        ----------
        Mw : float
            Moment magnitude.
        R_JB : float
            Joyner-Boore distance (km).
        Vs30 : float
            Vs30 (m/s).
        fault_type : str
            'strike_slip', 'normal', 'reverse', or 'unspecified'.
        period : float
            Spectral period in seconds (0.0 for PGA).

        Returns
        -------
        (median_g, sigma_ln)
        """
        c = _get_row(period)

        # ── Source term F_E ──
        F_E = self._source_term(Mw, fault_type, c)

        # ── Path term F_P ──
        F_P = self._path_term(Mw, R_JB, c)

        # ── Site term F_S (needs PGA_r for nonlinear) ──
        F_S = self._site_term(Mw, R_JB, Vs30, fault_type, c)

        # ── Aleatory sigma ──
        sigma = self._sigma(Mw, c)

        ln_y = F_E + F_P + F_S
        median_g = math.exp(ln_y)

        return median_g, sigma

    # ─── Internal term computations ──────────────────────────────────

    @staticmethod
    def _source_term(Mw: float, fault_type: str, c: np.ndarray) -> float:
        """Event term F_E — magnitude and mechanism dependent."""
        Mh = c[_CI["Mh"]]
        Mref = c[_CI["Mref"]]

        # Mechanism dummy variables
        if fault_type == "strike_slip":
            e_val = c[_CI["e1"]]
        elif fault_type == "normal":
            e_val = c[_CI["e2"]]
        elif fault_type == "reverse":
            e_val = c[_CI["e3"]]
        else:  # unspecified
            e_val = c[_CI["e0"]]

        e4 = c[_CI["e4"]]
        e5 = c[_CI["e5"]]
        e6 = c[_CI["e6"]]

        if Mw <= Mh:
            F_E = e_val + e4 * (Mw - Mh) + e5 * (Mw - Mh) ** 2
        else:
            F_E = e_val + e6 * (Mw - Mh)

        return float(F_E)

    @staticmethod
    def _path_term(Mw: float, R_JB: float, c: np.ndarray) -> float:
        """Path term F_P — distance and magnitude dependent."""
        Mref = c[_CI["Mref"]]
        Rref = c[_CI["Rref"]]
        h = c[_CI["h"]]
        c1 = c[_CI["c1"]]
        c2 = c[_CI["c2"]]
        c3 = c[_CI["c3"]]
        Dc3 = c[_CI["Dc3_global"]]  # global region; use Dc3_ChinaTurkey for Turkey

        R = math.sqrt(R_JB ** 2 + h ** 2)
        F_P = ((c1 + c2 * (Mw - Mref)) * math.log(R / Rref)
               + (c3 + Dc3) * (R - Rref))

        return float(F_P)

    def _site_term(
        self,
        Mw: float,
        R_JB: float,
        Vs30: float,
        fault_type: str,
        c: np.ndarray,
    ) -> float:
        """Site term F_S = F_lin + F_nl."""
        Vref = c[_CI["Vref"]]
        Vc = c[_CI["Vc"]]
        clin = c[_CI["clin"]]
        V1 = c[_CI["V1"]]
        V2 = c[_CI["V2"]]

        # Linear site term
        if Vs30 <= Vc:
            F_lin = clin * math.log(Vs30 / Vref)
        else:
            F_lin = clin * math.log(Vc / Vref)

        # Nonlinear site term (BSSA14 Eq. 6-7)
        f4 = c[_CI["f4"]]
        f5 = c[_CI["f5"]]
        f3 = c[_CI["f3"]]  # reference PGA for nonlinear term (0.1 for most periods)

        if abs(f4) < 1e-10:
            F_nl = 0.0
        else:
            # Compute PGA_r (PGA on reference rock, Vs30=760)
            pga_r = self._compute_pga_ref(Mw, R_JB, fault_type)
            # Vs30-dependent nonlinear coefficient
            F_nl = f4 * (math.exp(f5 * (min(Vs30, 760.0) - 360.0))
                         - math.exp(f5 * (760.0 - 360.0)))
            F_nl *= math.log((pga_r + f3) / f3)

        return float(F_lin + F_nl)

    def _compute_pga_ref(
        self, Mw: float, R_JB: float, fault_type: str
    ) -> float:
        """Compute PGA on reference rock (Vs30=760) for nonlinear site term.

        This is a self-referencing calculation: compute PGA with Vs30=760
        (which makes F_S = 0 since log(760/760) = 0 and nonlinear is ~0).
        """
        c_pga = _get_row(0.0)  # PGA coefficients
        F_E = self._source_term(Mw, fault_type, c_pga)
        F_P = self._path_term(Mw, R_JB, c_pga)
        # Site term on rock is zero (F_lin=0 since log(Vref/Vref)=0, F_nl≈0)
        return math.exp(F_E + F_P)

    @staticmethod
    def _sigma(Mw: float, c: np.ndarray) -> float:
        """Total aleatory sigma = sqrt(phi^2 + tau^2), M-dependent."""
        phi1 = c[_CI["phi1"]]
        phi2 = c[_CI["phi2"]]
        tau1 = c[_CI["tau1"]]
        tau2 = c[_CI["tau2"]]

        # Magnitude-dependent interpolation between M<=4.5 and M>=5.5
        if Mw <= 4.5:
            phi = phi1
            tau = tau1
        elif Mw >= 5.5:
            phi = phi2
            tau = tau2
        else:
            phi = phi1 + (phi2 - phi1) * (Mw - 4.5)
            tau = tau1 + (tau2 - tau1) * (Mw - 4.5)

        return float(math.sqrt(phi ** 2 + tau ** 2))


